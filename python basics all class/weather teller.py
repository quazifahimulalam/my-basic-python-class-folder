temp = int(input("enter the temperature in celcius:"))

if temp <= 16:
    print("it is winter time.")
elif temp <= 20 :
    print("it is late autumn")
elif temp <= 26 :
    print("it is spring time")
elif temp <= 27 :
    print("it is autumn ")    
elif temp <= 29 :
    print("it is monsoon ")
elif temp <= 33 :
    print("it is summer time")
else:
    print("it is summer time")
    